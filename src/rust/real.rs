pub fn w_im(x: f64) -> f64 {
    if x >= 0.0 {
        if x > 45.0 {
            // continued-fraction expansion is faster
            let ispi = 0.564_189_583_547_756_3; // 1 / sqrt(pi)
            if x > 5e7 {
                // 1-term expansion, important to avoid overflow
                return ispi / x;
            }
            /* 5-term expansion (rely on compiler for CSE), simplified from:
            ispi / (x-0.5/(x-1/(x-1.5/(x-2/x))))  */
            return ispi * ((x * x) * (x * x - 4.5) + 2.0) / (x * ((x * x) * (x * x - 5.0) + 3.75));
        }
        w_im_y100(100.0 / (1.0 + x), x)
    } else {
        // = -FADDEEVA(w_im)(-x)
        if x < -45.0 {
            // continued-fraction expansion is faster
            let ispi = 0.564_189_583_547_756_3; // 1 / sqrt(pi)
            if x < -5e7 {
                // 1-term expansion, important to avoid overflow
                return ispi / x;
            }
            /* 5-term expansion (rely on compiler for CSE), simplified from:
            ispi / (x-0.5/(x-1/(x-1.5/(x-2/x))))  */
            return ispi * ((x * x) * (x * x - 4.5) + 2.0) / (x * ((x * x) * (x * x - 5.0) + 3.75));
        }
        -w_im_y100(100.0 / (1.0 - x), -x)
    }
}

/// compute the error function erf(x)
pub fn erf(x: f64) -> f64 {
    let mx2 = -x * x;
    if mx2 < -750.0 {
        // underflow
        return if x >= 0.0 { 1.0 } else { -1.0 };
    }

    // Use Taylor series for small |x|, to avoid cancellation inaccuracy
    // erf(x) = 2/sqrt(pi) * x * (1 - x^2/3 + x^4/10 - x^6/42 + x^8/216 + ...)
    let taylor = || {
        x * (std::f64::consts::FRAC_2_SQRT_PI
            + mx2
                * (0.376_126_389_031_837_54
                    + mx2
                        * (0.112_837_916_709_551_26
                            + mx2 * (0.026_866_170_645_131_252 + mx2 * 0.005_223_977_625_442_188))))
    };

    if x >= 0.0 {
        if x < 8e-2 {
            return taylor();
        }
        1.0 - mx2.exp() * erfcx(x)
    } else {
        // x < 0
        if x > -8e-2 {
            return taylor();
        }
        mx2.exp() * erfcx(-x) - 1.0
    }
}

pub fn erfi(x: f64) -> f64 {
    if x * x > 720.0 {
        if x > 0.0 {
            f64::INFINITY
        } else {
            f64::NEG_INFINITY
        }
    } else {
        (x * x).exp() * w_im(x)
    }
}

/// erfc(x) = 1 - erf(x)
pub fn erfc(x: f64) -> f64 {
    if x * x > 750.0 {
        //underflow
        if x >= 0.0 {
            0.0
        } else {
            2.0
        }
    } else if x >= 0.0 {
        (-x * x).exp() * erfcx(x)
    } else {
        2.0 - (-x * x).exp() * erfcx(-x)
    }
}

pub fn erfcx(x: f64) -> f64 {
    if x >= 0.0 {
        if x > 50.0 {
            // continued-fraction expansion is faster
            if x > 5e7 {
                // 1-term expansion, important to avoid overflow
                return ISPI / x;
            }
            /* 5-term expansion (rely on compiler for CSE), simplified from:
            ISPI / (x+0.5/(x+1/(x+1.5/(x+2/x))))  */
            return ISPI * ((x * x) * (x * x + 4.5) + 2.0) / (x * ((x * x) * (x * x + 5.0) + 3.75));
        }
        erfcx_y100(400.0 / (4.0 + x))
    } else if x < -26.7 {
        f64::INFINITY
    } else if x < -6.1 {
        2.0 * (x * x).exp()
    } else {
        2.0 * (x * x).exp() - erfcx_y100(400.0 / (4.0 - x))
    }
}

// compute Dawson(x) = sqrt(pi)/2  *  exp(-x^2) * erfi(x)
pub fn dawson(x: f64) -> f64 {
    w_im(x) / std::f64::consts::FRAC_2_SQRT_PI
}

